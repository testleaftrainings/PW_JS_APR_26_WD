
//Arithmetic Operators  +, -,*, /,%

console.log(7/2); //3.5 Quotient
console.log(7%2); //1 Reminder

//Assignment Operators

let y = 10
y+=5 //y = y+5
console.log(y);

x = 30
x*=10 // x = x*10
console.log(x);

//post increment
//value++

let z = 10

z++;//10+1
console.log(z);

let a = 10
let b = 20
console.log(a+b);

//Unary Operators
//pre increment -->++value

console.log(++b);

//post decrement

let c = 10
console.log(c--)
console.log(c);//9

// pre decrement
console.log(--c); //1-9 =8

//=== strict equality operator both value and datatype will be checked
// == loose equality checks only the value not the data type
console.log(1===1); //true
console.log(1==="1")// false

console.log(1==true); //true

console.log(0===false);

console.log(null==undefined);//true
console.log(null===null); //false

















